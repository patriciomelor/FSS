<h1>AGENTE DE ADUANAS</h1>
<p>Es la persona autorizada por el Ministerio de Finanzas para actuar ante los &oacute;rganos competentes del servicio aduanero en nombre y por cuenta de aquel que contrata sus servicios, en el tr&aacute;mite de una operaci&oacute;n, r&eacute;gimen o actividad aduanera.
El Agente de Aduanas realiza una funci&oacute;n de naturaleza mercantil orientada a facilitar a quienes contraten sus servicios, es decir, a sus mandantes en todo lo relativo al cumplimiento de la normativa jur&iacute;dica vigente en materia de importaci&oacute;n, exportaci&oacute;n, tr&aacute;nsito aduanero y cualquier actividad accesoria inherente a dichas operaciones aduaneras.
En Chile, esa labor de intermediaci&oacute;n que desarrolla el Agente de Aduanas, constituye una actividad auxiliar a la funci&oacute;n P&uacute;blica Aduanera, pero ello no significa que posea alguna facultad p&uacute;blica delegada con su correspondiente autoridad coercitiva, por el contrario, recae sobre &eacute;ste un estricto control fiscal.
</p>

<strong>øQUI&eacute;N AUTORIZA EL FUNCIONAMIENTO DE LOS AGENTES DE ADUANAS?</strong>
1.- Cumplidos los requisitos exigidos en este cap&iacute;tulo, se expedir&aacute; al solicitante la autorizaci&oacute;n para actuar 
como agente de aduanas y se proceder&aacute; a inscribirlo en el registro correspondiente. 
La autorizaci&oacute;n se otorgar&aacute; mediante resoluci&oacute;n del Ministerio de Hacienda que se publicar&aacute; 
en la GACETA OFICIAL DE LA REP&uacute;BLICA DE Chile.</br>
2.- Cuando exista demora en el pago de las cantidades l&iacute;quidas y exigibles causadas con motivo del paso de mercanc&iacute;as a trav&eacute;s de las aduanas, &eacute;stas podr&aacute;n retener las dem&aacute;s que hayan llegado a nombre del mismo destinatario o consignatario, hasta que el pago se efect&uacute;e, sin perjuicio de los dem&aacute;s privilegios y acciones a que haya lugar de la aplicaci&oacute;n de los derechos de almacenaje y causales de abandono respectivo. En estos casos, no se dar&aacute; curso a escritos de designaci&oacute;n de consignatarios presentados por el deudor.</br>
El Reglamento determinar&aacute; la manera de hacer efectiva la presente disposici&oacute;n por todas las aduanas del pa&iacute;s.</br>
3.- Las autorizaciones otorgadas por el Ministerio de Hacienda a los agentes de aduanas para el ejercicio de sus funciones como tal, ser&aacute;n intransferibles.</br>
4.- S&oacute;lo podr&aacute;n actuar como agentes de aduanas las personas que hayan cumplido todos los requisitos y tr&aacute;mites establecidos en este cap&iacute;tulo.</br>

<strong>OBLIGACIONES DE LOS AGENTES DE ADUANAS</strong>
1) Indicar en todas las comunicaciones dirigidas a los organismos p&uacute;blicos en n&uacute;mero de registro de agente de aduanas.</br>
2) Deben designar por escrito las personas que tramitar&aacute;n en la aduana por al agencia.</br>
3) Deben presentar poder autenticado e excepcionalmente carta de poder, telegrama o telex, que le autorice a gestionar en nombre del interesado.</br>
4) Deben llevar un registro sellado y foliado por la oficina aduanera de la jurisdicci&oacute;n.</br>
5) La Direcci&oacute;n General de Aduanas del Ministerio de Hacienda llevar&aacute; un registro de agentes de aduanas, en el cual constar&aacute;:</br>
a) N&uacute;mero y fecha de la resoluci&oacute;n;</br>
b) N&uacute;mero de inscripci&oacute;n en dicho registro;</br>
c) Nombre o raz&oacute;n social del agente de aduanas;</br>
d) Autoridades ante las cuales se gestionar&aacute;;</br>
e) Otros datos que considere necesario el Ministerio de Hacienda.</br>
1.-Indicar&aacute;n en todas las comunicaciones que en tal car&aacute;cter dirijan a los organismos p&uacute;blicos, el n&uacute;mero de la inscripci&oacute;n en el registro se&ntilde;alado en el anterior.</br>
2.-Los que hayan sido autorizados para actuar o gestionar ante determinada oficina aduanera, podr&aacute;n hacerlo tambi&eacute;n en otras oficinas aduaneras, siempre que se realicen en virtud de acatamiento a disposiciones de emergencia, dictadas por organismos p&uacute;blicos autorizados.</br>
3.-La ampliaci&oacute;n de las gestiones autorizadas a los agentes de aduanas, requerir&aacute; aprobaci&oacute;n previa de la Direcci&oacute;n General de Aduanas, a cuyo efecto regir&aacute;n las disposiciones de este capitulo que fueren aplicables.</br>
4.- Las personas jur&iacute;dicas deber&aacute;n mantener a su servicio en la agencia principal y en cada sucursal que tuvieren, uno o m&aacute;s empleados capacitados en materia aduanera.</br>
5.- Deber&aacute;n designar por escrito, una o varias personas naturales para que presenten solicitudes, recursos o reclamos, reciban documentos y, en general, para que act&uacute;en ante determinada oficina aduanera, siendo responsables, conforme al derecho com&uacute;n, por actuaciones de dichos empleados. Estas designaciones se entregar&aacute;n en original, con el nombre y la firma aut&eacute;ntica de los empleados designados y se insertar&aacute;n en un registro de firmas autorizadas que deber&aacute; llevar cada oficina aduanera.</br>
6.- Los jefes de las oficinas aduaneras no podr&aacute;n admitir actuaciones de agentes de aduanas o de personas autorizadas por &eacute;stos, que no posean la representaci&oacute;n, conforme a las disposiciones que anteceden.</br>
7.- Para actuar como tal, requerir&aacute; poder aut&eacute;ntico otorgado por el consignatario, exportador o persona interesada. No obstante, en casos excepcionales, la autoridad aduanera competente, podr&aacute; aceptar una carta poder, telegrama o t&eacute;lex, siempre que las circunstancias concurrentes as&iacute; lo justifiquen.</br>
8.- Cada oficina aduanera, deber&aacute; llevar un registro actualizado de los poderes permanentes, de los cuales deber&aacute; entregarse el original o copia certificada del mismo, y dem&aacute;s documentos mediante los cuales se haya conferido la respectiva representaci&oacute;n.</br>
Los documentos a que se refiere este deber&aacute;n constar en original o copia certificada.</br>
9.- Deber&aacute;n llevar un registro en un libro previamente sellado y foliado por la oficina aduanera de la jurisdicci&oacute;n, en el cual asentar&aacute;n ordenadamente todos los datos relativos a: n&uacute;mero de declaraci&oacute;n de aduanas, nombre del veh&iacute;culo y fecha de llegada y n&uacute;mero de las planillas de liquidaci&oacute;n y fecha de pago.</br>

<strong>ACTIVIDADES ADUANERAS</strong>

Son todas aquellas actividades que le van a permitir al Agente de Aduanas a trav&eacute;s de sus tramitadores y personal, preparar todo lo pertinente a la nacionalizaci&oacute;n o desaduanamiento de las mercanc&iacute;as.</br>
Dentro de ellas podemos se&ntilde;alar:</br>
Visitar el almac&eacute;n para ver en que condiciones lleg&oacute; la mercanc&iacute;a.</br>
Ir a la entidad bancaria para saber si el cliente deposit&oacute; el pago de impuestos o la planilla de liquidaci&oacute;n.</br>
